//11. Write a Java program to demonstrate the use
// of both pre-increment and post-decrement 
//operators in a single expression 


class PreIncrePostDecre{
 public static void main(String args[]){
   int a = 10; 
 int b = 5; 
 int result = ++a * b-- ;
 System.out.println(result); 
 } 

} 
